#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	c;

	i = 0;
	while (argv[0][i] != '\0')
	{
		write(1, &argv[0][i], 1);
		i ++;
	}
	c = '\n';
	i = argc;
	write(1, &c, 1);
	return (0);
}
